package ads

import (
	"testing"
)

func TestNewRead(t *testing.T) {
	p := NewRead([6]byte{0x05, 0x16, 0xa7, 0x68, 0x01, 0x01}, 8451,
		[6]byte{0xa9, 0xfe, 0xa4, 0xa3, 0x01, 0x01}, 30336, 16416, 0, 1)
	t.Log(p.Bytes().HEX())
}

func TestNewWrite(t *testing.T) {
	p := NewWrite([6]byte{0x05, 0x16, 0xa7, 0x68, 0x01, 0x01}, 8451,
		[6]byte{0xa9, 0xfe, 0xa4, 0xa3, 0x01, 0x01}, 30336, 16416, 0, []byte{0x01})
	t.Log(p.Bytes().HEX())
}
